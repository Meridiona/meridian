"""Generate activity reports for 10 more hours to expand the labeled eval set.

Reports are generated by Qwen3.5-2B (think mode), same as gen_labeled_reports.py.
Output → labeled_reports_extra.json (unlabeled). Claude labels each afterward,
then they merge into labeled_reports.json.
"""
from __future__ import annotations
import gc, json, sys, time
from pathlib import Path

ROOT = Path(__file__).parent
SERVICES = ROOT.parents[2]
sys.path.insert(0, str(SERVICES))

from agents.session_distiller import distil_hour, evict_embedder  # noqa: E402

OUT = ROOT / "labeled_reports_extra.json"
MODEL_ID = "mlx-community/Qwen3.5-2B-OptiQ-4bit"

# (hour, daily_plan, note) — plans reflect each day's active tickets.
HOURS = [
    ("2026-06-19T18", ["KAN-109", "KAN-200", "KAN-231"],            "jun19 evening"),
    ("2026-06-21T08", ["KAN-200", "KAN-109", "KAN-242", "KAN-243"], "jun21 morning multi"),
    ("2026-06-21T15", ["KAN-200", "KAN-242", "KAN-243"],            "jun21 afternoon no-hint"),
    ("2026-06-22T04", ["KAN-243", "KAN-242", "KAN-200"],            "jun22 early no-hint"),
    ("2026-06-22T09", ["KAN-243", "KAN-242", "KAN-200", "KAN-109"], "jun22 morning multi"),
    ("2026-06-22T11", ["KAN-243", "KAN-242", "KAN-200"],            "jun22 midday"),
    ("2026-06-23T05", ["KAN-243", "KAN-200", "KAN-239"],            "jun23 early"),
    ("2026-06-23T14", ["KAN-243", "KAN-200", "KAN-239"],            "jun23 afternoon no-hint"),
    ("2026-06-23T17", ["KAN-243", "KAN-200", "KAN-239"],            "jun23 evening no-hint"),
    ("2026-06-24T04", ["KAN-243", "KAN-200", "KAN-239"],            "jun24 early no-hint"),
]


def generate_report(body: str) -> tuple[str, int, float]:
    import mlx.core as mx
    from mlx_lm import generate, load
    from mlx_lm.sample_utils import make_logits_processors, make_sampler
    from agents.prompts.activity_report import SYSTEM

    model, tok = load(MODEL_ID)
    try:
        sampler = make_sampler(temp=1.0, top_p=0.95, top_k=20)
        lp = make_logits_processors(repetition_penalty=1.1, repetition_context_size=64,
                                    presence_penalty=1.5)
        messages = [{"role": "system", "content": SYSTEM}, {"role": "user", "content": body}]
        ids = tok.apply_chat_template(messages, add_generation_prompt=True, enable_thinking=True)
        if hasattr(ids, "keys") and "input_ids" in ids:
            ids = ids["input_ids"]
        t0 = time.monotonic()
        raw = generate(model, tok, prompt=ids, max_tokens=32768, sampler=sampler,
                       logits_processors=lp, verbose=False)
        elapsed = round(time.monotonic() - t0, 1)
        if "</think>" in raw:
            raw = raw.split("</think>", 1)[1]
        return raw.strip(), len(ids), elapsed
    finally:
        del model, tok
        gc.collect()
        mx.clear_cache()


def main() -> None:
    results = []
    for i, (hour, plan, note) in enumerate(HOURS):
        print(f"\n[{i+1}/{len(HOURS)}] {hour} plan={plan} ({note})", flush=True)
        body, ds = distil_hour(hour)
        if not body:
            print("  SKIP — no sessions")
            continue
        print(f"  distil: {ds.nsess} sess, {ds.out_chars} chars", flush=True)
        report, in_tok, elapsed = generate_report(body)
        print(f"  report: {len(report)} chars, {elapsed}s", flush=True)
        print(f"--- REPORT ({hour}) ---\n{report}\n--- END ---", flush=True)
        results.append({"hour": hour, "daily_plan": plan, "note": note, "report": report,
                        "nsess": ds.nsess, "label": None, "label_note": ""})
        OUT.write_text(json.dumps(results, indent=2))
    evict_embedder()
    print(f"\nGENERATED {len(results)} → {OUT.name}")


if __name__ == "__main__":
    main()
