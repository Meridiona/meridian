"""INFORMATION-LOSS AUDIT for the reduced hour session-text.

Gold = each app_session's own session_summary (independently generated by the production
classifier from the FULL session_text) — fallback task_reasoning. For each peak hour we
build the reduced hour-text, then an LLM judge checks whether every gold session's
worklog-relevant facts are still present/inferable in the reduced text.

Two layers:
  1. DETERMINISTIC dropout: gold sessions with ZERO surviving spans = guaranteed loss.
  2. LLM JUDGE (qwen3.5:4b): per gold session -> FULL / PARTIAL / MISSING + the missing facts.

Recall = (FULL + 0.5*PARTIAL) / total_gold. Missing facts are dumped so we can SEE the loss.

Usage: python audit_hour.py [N_hours] [judge_model]
"""
import sys, os, json, re, time, subprocess, sqlite3, urllib.request
from collections import Counter, defaultdict
import clib
from hour_text import build_hour_text, EXCLUDE, MIN_DUR

OLLAMA = "http://127.0.0.1:11434"
N_HOURS = int(sys.argv[1]) if len(sys.argv) > 1 else 12
JUDGE = sys.argv[2] if len(sys.argv) > 2 else "qwen3.5:4b"
SELECT_MODE = sys.argv[3] if len(sys.argv) > 3 else "floc"


def peak_hours(n):
    con = sqlite3.connect(clib.DB)
    rows = con.execute(
        f"""SELECT substr(started_at,1,13) h, COUNT(*) c,
                   SUM(CASE WHEN session_summary IS NOT NULL AND LENGTH(session_summary)>5 THEN 1 ELSE 0 END) g
            FROM app_sessions
            WHERE app_name NOT IN ({','.join('?'*len(EXCLUDE))})
              AND duration_s >= ? AND session_text IS NOT NULL AND LENGTH(session_text)>40
              AND started_at >= date('now','-7 days')
            GROUP BY h HAVING g >= 5 ORDER BY c DESC LIMIT ?""",
        (*EXCLUDE, MIN_DUR, n)).fetchall()
    con.close()
    return [r[0] for r in rows]


def gold_for_hour(hour):
    con = sqlite3.connect(clib.DB)
    rows = con.execute(
        f"""SELECT id, app_name, task_key, task_session_type,
                   COALESCE(NULLIF(session_summary,''), task_reasoning, '')
            FROM app_sessions
            WHERE app_name NOT IN ({','.join('?'*len(EXCLUDE))})
              AND duration_s >= ? AND session_text IS NOT NULL AND LENGTH(session_text)>40
              AND started_at LIKE ? ORDER BY started_at""",
        (*EXCLUDE, MIN_DUR, hour + "%")).fetchall()
    con.close()
    return [{"sid": r[0], "app": r[1], "tk": r[2], "type": r[3], "gold": r[4]}
            for r in rows if r[4] and len(r[4]) > 5]


JUDGE_PROMPT = """You audit whether a COMPRESSED hour-of-activity text still lets a reader
recover what each session was about. Below is the compressed text, then GROUND-TRUTH session
summaries (each independently written from the full screen capture).

For EACH session, judge ONLY whether the compressed text lets you recover what THAT session's
summary describes — NOT whether the work was important.
- FULL: you can tell this session/topic happened and recover its key named details (files,
  tickets, the specific page/feature/decision the summary mentions).
- PARTIAL: you can tell the topic happened, but a SPECIFIC named detail in the summary (a file
  path, a ticket key, a concrete decision/result) is absent from the compressed text.
- MISSING: you could not tell from the compressed text that this session/topic happened at all.

CRITICAL: if the summary says the session was idle / overhead / browsing / watching a video /
remote-desktop / settings, and the compressed text shows that same app or activity, that is
FULL. "Low-value activity" is NOT "missing". Only judge recoverability of what the summary says.
If the summary names NO specific ticket/file, do not penalise for a missing ticket/file.

Output ONLY a JSON array, one object per session, in order:
[{{"sid": <id>, "verdict": "FULL|PARTIAL|MISSING", "lost": "<specific named detail lost, or empty>"}}]

=== COMPRESSED HOUR TEXT ===
{body}

=== GROUND-TRUTH SESSIONS ({n}) ===
{gold}

=== JSON AUDIT ==="""


def _call(prompt):
    opts = {"temperature": 0.0, "num_ctx": 32768, "num_predict": 1200}
    bd = {"model": JUDGE, "prompt": prompt, "stream": False, "options": opts, "keep_alive": "120s"}
    if JUDGE.startswith("qwen3"):
        bd["think"] = False
    req = urllib.request.Request(OLLAMA + "/api/generate", data=json.dumps(bd).encode(),
                                 headers={"Content-Type": "application/json"})
    with urllib.request.urlopen(req, timeout=900) as r:
        out = json.loads(r.read())
    txt = out.get("response", "")
    m = re.search(r"\[.*\]", txt, re.S)
    if not m:
        return []
    try:
        return json.loads(m.group(0))
    except Exception:
        objs = re.findall(r"\{[^{}]*\}", m.group(0))
        res = []
        for o in objs:
            try: res.append(json.loads(o))
            except Exception: pass
        return res


def judge(body, golds, chunk=8):
    """Chunk gold sessions so the JSON output never truncates."""
    verdicts = []
    for i in range(0, len(golds), chunk):
        part = golds[i:i + chunk]
        gold_str = "\n".join(
            f'- sid {g["sid"]} [{g["app"]}{"/"+g["tk"] if g["tk"] else ""}]: {g["gold"][:300]}'
            for g in part)
        prompt = JUDGE_PROMPT.format(body=body[:40000], n=len(part), gold=gold_str)
        verdicts.extend(_call(prompt))
    return verdicts, None


def main():
    hours = peak_hours(N_HOURS)
    print(f"Auditing {len(hours)} peak hours with judge={JUDGE}\n{hours}\n")
    subprocess.run(["ollama", "stop", JUDGE], capture_output=True)
    grand = Counter(); all_lost = []
    # worklog-critical subset = task sessions that carry a ticket
    crit = Counter()
    bytype = defaultdict(Counter)
    per_hour = []
    for hour in hours:
        # free the judge's GPU memory so the MPS embedder doesn't thrash swap while building
        subprocess.run(["ollama", "stop", JUDGE], capture_output=True)
        body, st = build_hour_text(hour, select_mode=SELECT_MODE)
        golds = gold_for_hour(hour)
        if not golds:
            continue
        survived = set(st["sids_with_spans"])
        dropped = [g for g in golds if g["sid"] not in survived]
        t0 = time.time()
        verdicts, raw = judge(body, golds)
        dt = time.time() - t0
        vmap = {v.get("sid"): v for v in verdicts if isinstance(v, dict)}
        c = Counter()
        lost_here = []
        for g in golds:
            v = vmap.get(g["sid"]) or vmap.get(str(g["sid"]))
            # a deterministically-dropped session is MISSING regardless of judge
            if g["sid"] not in survived:
                verdict = "MISSING"; lost = "(session has zero surviving spans)"
            elif v:
                verdict = v.get("verdict", "?").upper(); lost = v.get("lost", "")
            else:
                verdict = "?"; lost = ""
            c[verdict] += 1
            bytype[g["type"] or "?"][verdict] += 1
            if g["type"] == "task" and g["tk"]:
                crit[verdict] += 1
            if verdict in ("PARTIAL", "MISSING") and lost:
                tag = f'{g["app"]}{"/"+g["tk"] if g["tk"] else ""}|{g["type"]}'
                lost_here.append(f'  [{hour} sid{g["sid"]} {tag}] {verdict}: {lost}')
        grand.update(c)
        tot = sum(c.values())
        recall = (c["FULL"] + 0.5 * c["PARTIAL"]) / tot if tot else 0
        red = 100 * (1 - st["out_chars"] / max(st["raw_chars"], 1))
        print(f"── {hour}  {st['nsess']}s {st['raw_chars']//1000}k→{st['out_chars']//1000}k ({red:.0f}%)  "
              f"gold={tot} dropout={len(dropped)}  "
              f"FULL={c['FULL']} PARTIAL={c['PARTIAL']} MISSING={c['MISSING']} "
              f"recall={recall:.0%}  ({dt:.0f}s)")
        per_hour.append((hour, recall, c, red))
        all_lost.extend(lost_here)
    subprocess.run(["ollama", "stop", JUDGE], capture_output=True)

    tot = sum(grand.values())
    recall = (grand["FULL"] + 0.5 * grand["PARTIAL"]) / tot if tot else 0
    ct = sum(crit.values())
    crecall = (crit["FULL"] + 0.5 * crit["PARTIAL"]) / ct if ct else 0
    print(f"\n{'='*72}\nGRAND TOTAL  gold={tot}  FULL={grand['FULL']} "
          f"PARTIAL={grand['PARTIAL']} MISSING={grand['MISSING']}  RECALL={recall:.1%}")
    print(f"WORKLOG-CRITICAL (task+ticket)  n={ct}  FULL={crit['FULL']} "
          f"PARTIAL={crit['PARTIAL']} MISSING={crit['MISSING']}  RECALL={crecall:.1%}")
    print("\nBY SESSION TYPE:")
    for ty, cc in sorted(bytype.items(), key=lambda x: -sum(x[1].values())):
        n = sum(cc.values()); rc = (cc["FULL"] + 0.5 * cc["PARTIAL"]) / n if n else 0
        print(f"  {ty:11} n={n:3}  FULL={cc['FULL']:3} PARTIAL={cc['PARTIAL']:3} "
              f"MISSING={cc['MISSING']:3} unparsed={cc['?']:2}  recall={rc:.0%}")
    print(f"\n{'='*72}\nLOST / PARTIAL FACTS (the actual information loss):")
    for l in all_lost:
        print(l)


if __name__ == "__main__":
    main()
